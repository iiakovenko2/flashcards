let currentStack = [];
let currentIndex = 0;

function shuffleArray(array) {
    for (let i = array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [array[i], array[j]] = [array[j], array[i]];
    }
}

function startStack(name) {
    currentStack = [...data[name]];
    shuffleArray(currentStack);
    currentIndex = 0;
    document.getElementById('category-title').innerText = name;
    document.getElementById('menu').style.display = 'none';
    document.getElementById('player').style.display = 'block';
    updateCard();
}

function updateCard() {
    const card = currentStack[currentIndex];
    const textEl = document.getElementById('q-text-content');
    const imgEl = document.getElementById('q-img');
    const aEl = document.getElementById('a-text');
    
    document.querySelector('.flashcard').classList.remove('flipped');
    
    textEl.innerHTML = card.q;
    aEl.innerHTML = card.a;

    // Handle Image display
    if (card.img) {
        imgEl.src = card.img;
        imgEl.style.display = "block";
    } else {
        imgEl.style.display = "none";
    }

    document.getElementById('progress').innerText = `${currentIndex + 1} / ${currentStack.length}`;
    
    // Render Math
    renderMathInElement(document.getElementById('player'), {
        delimiters: [{left: '$', right: '$', display: false}]
    });
}

function nextCard() {
    currentIndex++;
    if (currentIndex >= currentStack.length) {
        currentIndex = 0;
        shuffleArray(currentStack); // Reshuffle for endless loop
    }
    updateCard();
}

